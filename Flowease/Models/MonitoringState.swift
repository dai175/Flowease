import Foundation

/// 姿勢監視の状態
///
/// アプリケーションの姿勢監視機能の現在の状態を表す。
/// 状態によってメニューバーアイコンの表示が変わる。
enum MonitoringState: Equatable {
    /// 正常に監視中
    ///
    /// カメラで人物を検出し、姿勢スコアを算出できている状態。
    /// アイコンはスコアに応じた色で表示される。
    case active(PostureScore)

    /// 一時停止
    ///
    /// 一時的に監視が中断されている状態。自動的に復帰する可能性がある。
    /// アイコンはグレーで表示される。
    case paused(PauseReason)

    /// 無効
    ///
    /// 監視機能が無効化されている状態。ユーザーの操作が必要。
    /// アイコンはグレーで表示され、対処法が案内される。
    case disabled(DisableReason)
}
